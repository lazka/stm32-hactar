// taken from the linux kernel
uint16_t crc16(uint16_t crc, uint8_t *buffer, size_t len)
{
    while (len--)
        crc = (crc >> 8) ^ crc16_table[(crc ^ *buffer++) & 0xff];
    return crc;
}

// To use this you have to enable the CRC unit's clock first
// RCC_AHBPeriphClockCmd(RCC_AHBENR_CRCEN, ENABLE);
uint32_t crc32(uint32_t crc, uint8_t *buffer, size_t len)
{
    assert(RCC->AHBENR | RCC_AHBENR_CRCEN);
    assert(!(len % 4));

    CRC->CR = CRC_CR_RESET;
    CRC->DR = crc;
    for(; len; len -= 4)
        CRC->DR = *((uint32_t*)buffer++);

    return CRC->DR;
}
